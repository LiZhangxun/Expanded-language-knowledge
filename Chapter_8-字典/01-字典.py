"""
    字典里面的数据是以键值对的形式出现，字典数据和数据顺序无关 --> 即字典不支持下标
    字典查找数据需要按照对应的键名去查找
    字典特点：
        1.符合为大括号{}
        2.数据为键值对 --> key:value
        3.各个键值对以逗号,隔开
"""

# 有数据字典
dict1 = {'name': 'Tom', 'sex': '男', 'age': 18}
print(dict1)

# 空字典
dict2 = {} # 手动创建
dict3 = dict() # 利用dict()函数创建
print(dict2)
print(dict3)
