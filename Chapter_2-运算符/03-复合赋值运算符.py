"""
几种复合赋值运算符：
1. += --> c+=a --> c=c+a
2. -= --> c-=a --> c=c-a
3. *= --> c*=a --> c=c*a
4. /= --> c/=a --> c=c/a
5. //= --> c//=a --> c=c//a
6. %= --> c%=a --> c=c%a
7. **= --> c**=a --> c=c**a
"""
c = 10
# c+=3 , c=c+3 √
# c=c+1+2 ×
c += 1+2
print(c)

# 先算复合赋值运算符右侧的表达式，再算复合赋值运算
# d*=3 , c=d*3
d = 10
d *= 1+2
print(d)
